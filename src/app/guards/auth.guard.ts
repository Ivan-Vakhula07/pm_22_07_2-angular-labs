import { inject } from '@angular/core';
import { CanActivateFn, Router } from '@angular/router';

// --- ПУНКТ 3: Реалізація Angular Guard для захисту маршрутів ---
// Використання CanActivateFn для контролю доступу до сторінок
export const authGuard: CanActivateFn = (route, state) => {
  const router = inject(Router);

  // --- ПУНКТ 3: Перевірка стану автентифікації користувача ---
  // Зчитування прапорця авторизації з локального сховища браузера (localStorage)
  const isLoggedIn = localStorage.getItem('isLoggedIn') === 'true';

  if (isLoggedIn) {
    // --- ПУНКТ 3: Надання доступу до захищеного маршруту ---
    // Якщо статус автентифікації позитивний, Guard дозволяє активацію компонента
    return true;
  } else {
    // --- ПУНКТ 3: Функціонал блокування неавтентифікованих користувачів ---
    // Виведення системного повідомлення про обмеження доступу
    alert('Доступ заборонено!');

    // --- ПУНКТ 3: Автоматичне перенаправлення на сторінку входу ---
    // Забезпечення навігації до компонента автентифікації при спробі несанкціонованого входу
    router.navigate(['/login']);

    // Блокування переходу на запитуваний маршрут (повернення значення false)
    return false;
  }
};
